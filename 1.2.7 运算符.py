# 算术（数学）运算符
print(1 + 1)  # 加法
print(1 - 1)  # 减法
print(1 * 1)  # 乘法
print(1 / 1)  # 除法
print(1 // 1)  # 取整除
print(1 % 1)  # 取余
print(1 ** 2)  # 指数（表示a的b次方，这里表示1的2次方）

# 赋值运算符
num = 1 + 2 * 3

# 复合赋值运算符
num = 1
num += 1  # num = num + 1
print(num)
num -= 1  # num = num - 1
print(num)
num *= 4  # num = num * 4
print(num)
num /= 2  # num = num / 2
print(num)
num %= 2  # num = num % 2
print(num)
