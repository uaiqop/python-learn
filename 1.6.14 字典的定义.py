"""
字典的格式：key:value
通过key找出value
"""

# 定义字典
a = {"qq": "3363911732", "昵称": "Aaron", "手机": "15915577103"}
print(a)
# 如果有重复的key，那么取前面那个的值

# 字典获取value
print(a["昵称"])
print(a["qq"])
print(a["手机"])

# 字典的嵌套
a = {
    "a": {
        "语文": "88",
        "数学": "78",
        "英语": "69"
    }, "b": {
        "语文": "80",
        "数学": "88",
        "英语": "99"
    }, "c": {
        "语文": "87",
        "数学": "40",
        "英语": "20"
    }
}

print(a["c"]["数学"])
