"""
布尔属于数字类型
True或1为真
False或0为假
"""
result = 10 > 5
print(result, type(result))

result = "hello" == "hello1"  # ==为比较运算符
print(result)

"""
比较运算符
== 判断内容是否相等
!= 判断内容是否不相等
> 判断左侧是否大于右侧
< 右侧是否大于左侧
>= 右侧是否大于等于左侧
<= 右侧是否小于等于左侧
"""
print(10 == 10)
print(10 != 10)
print(10 > 5)
print(7 <= 7)
